// 메인 실행
async function main() {
  console.log('네트워크관리사2급 2024년 정기 1~4회 정답+해설 업데이트 시작');
  console.log('='.repeat(60));

  let totalUpdated = 0;

  for (const examId of [152, 153, 154, 155]) {
    const res = await query(
      'SELECT id, exam_id, question_number, body, choices FROM questions WHERE exam_id=$1 ORDER BY question_number',
      [examId]
    );

    const examAnswers = answers[examId];
    if (!examAnswers || examAnswers.length !== res.rows.length) {
      console.error(`exam_id ${examId}: 문제 수 불일치! DB=${res.rows.length}, 정답=${examAnswers?.length}`);
      continue;
    }

    let examUpdated = 0;
    for (let i = 0; i < res.rows.length; i++) {
      const q = res.rows[i];
      const answer = examAnswers[i];

      if (answer < 1 || answer > 4) {
        console.error(`exam_id ${examId} Q${q.question_number}: 잘못된 정답 ${answer}`);
        continue;
      }

      const explanation = generateExplanation(q, answer);

      await query(
        'UPDATE questions SET answer=$1, explanation=$2, updated_at=NOW() WHERE id=$3',
        [answer, explanation, q.id]
      );
      examUpdated++;
    }

    console.log(`exam_id ${examId} (${res.rows[0]?.exam_id === 152 ? '1회' : res.rows[0]?.exam_id === 153 ? '2회' : res.rows[0]?.exam_id === 154 ? '3회' : '4회'}): ${examUpdated}문제 업데이트 완료`);
    totalUpdated += examUpdated;
  }

  console.log('='.repeat(60));
  console.log(`총 ${totalUpdated}문제 완료`);

  await getPool().end();
}

main().catch(err => {
  console.error('오류 발생:', err);
  process.exit(1);
});
