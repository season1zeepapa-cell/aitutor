// 해설 생성 함수
function generateExplanation(question, answer) {
  const choices = typeof question.choices === 'string'
    ? JSON.parse(question.choices)
    : question.choices;

  const correctChoice = choices.find(c => c.num === answer);
  const wrongChoices = choices.filter(c => c.num !== answer);

  const correctText = correctChoice?.text || '';
  const numLabel = ['', '①', '②', '③', '④'];

  // 정답 라인
  let html = `<p class="exp-answer">✅ 정답: <strong>${numLabel[answer]} ${correctText}</strong></p>`;

  // 해설 본문 생성
  const expBody = getExplanationBody(question, answer, choices);
  html += `<div class="exp-section"><div class="exp-section-title">📖 해설</div><p>${expBody}</p></div>`;

  // 오답 분석
  const wrongAnalysis = wrongChoices.map(c => {
    const reason = getWrongReason(question, answer, c, choices);
    return `<p>${numLabel[c.num]} ${c.text} — ${reason}</p>`;
  }).join('');
  html += `<div class="exp-section"><div class="exp-section-title">❌ 오답 분석</div>${wrongAnalysis}</div>`;

  return html;
}

