// 메인 실행
async function main() {
  let totalUpdated = 0;
  const examIds = [136, 137, 138, 139];

  for (const examId of examIds) {
    console.log(`\n=== exam_id ${examId} 처리 시작 ===`);

    // 문제 조회
    const res = await query(
      'SELECT id, exam_id, question_number, body, choices FROM questions WHERE exam_id=$1 ORDER BY question_number',
      [examId]
    );

    const examAnswers = answers[examId];
    if (!examAnswers || examAnswers.length !== res.rows.length) {
      console.error(`exam_id ${examId}: 정답 수(${examAnswers?.length})와 문제 수(${res.rows.length}) 불일치!`);
      continue;
    }

    let updated = 0;
    for (let i = 0; i < res.rows.length; i++) {
      const q = res.rows[i];
      const answer = examAnswers[i];

      if (answer < 1 || answer > 4) {
        console.error(`exam_id ${examId} Q${q.question_number}: 잘못된 정답 ${answer}`);
        continue;
      }

      // 해설 생성
      const explanation = generateExplanation(
        { ...q, exam_id: examId },
        answer
      );

      // DB 업데이트
      await query(
        'UPDATE questions SET answer=$1, explanation=$2, updated_at=NOW() WHERE id=$3',
        [answer, explanation, q.id]
      );

      updated++;
      if (updated % 10 === 0) {
        process.stdout.write(`  ${updated}/${res.rows.length} 완료\r`);
      }
    }

    console.log(`  exam_id ${examId}: ${updated}문제 업데이트 완료`);
    totalUpdated += updated;
  }

  console.log(`\n총 ${totalUpdated}문제 완료`);
  await getPool().end();
}

main().catch(err => {
  console.error('오류 발생:', err);
  process.exit(1);
});
